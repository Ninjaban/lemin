#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ptr;

	ptr = b;
	while (len)
	{
		*(ptr + len - 1) = (unsigned char)c;
		len--;
	}
	return (b);
}
