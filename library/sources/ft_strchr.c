#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int		n;

	n = 0;
	if (!s)
		return (NULL);
	while (s[n] && s[n] != c)
		n = n + 1;
	if (s[n] == c)
		return ((char *)(s + n));
	return (NULL);
}
