#include "libft.h"

char		*ft_revstr(char *str)
{
	char	*rev;
	int		n;
	int		i;

	n = 0;
	i = ft_strlen(str) - 1;
	if ((rev = ft_malloc(ft_strlen(str) + 1)) == NULL)
		return (NULL);
	while (i >= 0)
		rev[n++] = str[i--];
	rev[n] = '\0';
	ft_free(str);
	return (rev);
}
