#include "libft.h"

t_btree		*btree_create_node(void *item)
{
	t_btree	*new;

	if (!item)
		return (NULL);
	if ((new = ft_malloc(sizeof(t_btree))) == NULL)
		return (NULL);
	new->item = item;
	new->left = NULL;
	new->right = NULL;
	return (new);
}
