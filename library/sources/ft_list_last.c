#include <stdlib.h>
#include "libft.h"

t_lst	*ft_list_last(t_lst *begin_list)
{
	t_lst	*last;

	last = begin_list;
	if (begin_list == NULL)
		return (NULL);
	while (last->next != NULL)
		last = last->next;
	return (last);
}
