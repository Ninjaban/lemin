#include "_malloc.h"

void		*reallocf(void *ptr, size_t size)
{
	void	*new_ptr;

	if (!(new_ptr = realloc(ptr, size)))
	{
		ft_free(ptr);
		return (NULL);
	}
	return (new_ptr);
}
