#include "libft.h"

void		*ft_meft_malloc(size_t size)
{
	char	*ptr;

	if ((ptr = ft_malloc(size)) == NULL)
		return (NULL);
	while (--size != 0)
		ptr[size] = 0;
	ptr[0] = 0;
	return ((void *)ptr);
}
