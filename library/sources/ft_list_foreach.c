#include "libft.h"

void	ft_list_foreach(t_lst *begin_list, void (*f)(void *))
{
	t_lst	*elem;

	elem = begin_list;
	while (elem != NULL)
	{
		(*f)(elem->data);
		elem = elem->next;
	}
}
