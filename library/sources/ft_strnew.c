#include "libft.h"

char		*ft_strnew(size_t size)
{
	char	*str;
	size_t	n;

	n = 0;
	if (size == 0)
		return (NULL);
	if ((str = ft_malloc(size + 1)) == NULL)
		return (NULL);
	while (n <= size)
		str[n++] = '\0';
	return (str);
}
