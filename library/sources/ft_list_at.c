#include "libft.h"

t_lst	*ft_list_at(t_lst *begin_list, unsigned int nbr)
{
	t_lst			*elem;
	unsigned int	n;

	n = 0;
	if ((elem = begin_list) == NULL)
		return (NULL);
	while (n < nbr && elem->next != NULL)
	{
		elem = elem->next;
		n = n + 1;
	}
	if (n < nbr)
		return (NULL);
	return (elem);
}
