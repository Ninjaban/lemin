#include "libft.h"

char		*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		n;

	n = -1;
	if (s == NULL)
		return (NULL);
	if ((str = ft_malloc(ft_strlen((char *)s) + 1)) == NULL)
		return (NULL);
	while (s[++n])
		str[n] = f(s[n]);
	str[n] = '\0';
	return (str);
}
