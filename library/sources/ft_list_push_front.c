#include <stdlib.h>
#include "libft.h"

void	ft_list_push_front(t_lst **begin_list, void *data)
{
	t_lst	*new_elem;
	t_lst	*elem;

	new_elem = ft_create_elem(data);
	elem = *begin_list;
	new_elem->next = elem;
	*begin_list = new_elem;
}
